function validateEmail(){
  var typedmail = document.getElementById('email').value
    if(!typedmail.match((/^[A-Za-z\._\-0-9]*[@][A-Za-z]*[\.][a-z]{2,4}$/))){
      $('#email-response').html("Invalid Email Address")
      document.getElementById('submit-btn').style.display='none'
                
    }else{
      document.getElementById('submit-btn').style.display='block'
      $('#email-response').html("")
    }
}
